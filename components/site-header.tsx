function isActive(pathname: string, href: string) {
  if (href === "/") return pathname === "/";
  return pathname === href || pathname.startsWith(`${href}/`);
}

export function SiteHeader() {
  const pathname = usePathname();

  return (
    <header className="relative z-50">
      <div className="mx-auto flex max-w-5xl justify-center px-4 pt-8 pb-1 sm:px-6 sm:pt-12 sm:pb-2 md:pt-16 md:pb-4">
        <nav
          className="flex flex-wrap items-center justify-center gap-1 sm:gap-2 md:gap-3"
          aria-label="Main"
        >
          {navItems.map(({ href, label, icon }) => {
            const active = isActive(pathname, href);
            return (
              <Link
                key={href}
                href={href}
                aria-label={label}
                title={label}
                className={`rounded-full p-3 text-muted transition-colors duration-200 ease-out hover:text-foreground sm:p-3.5 md:p-4 ${
                  active ? "text-foreground ring-1 ring-white/15 bg-white/[0.04]" : ""
                }`}
              >
                {icon}
              </Link>
            );
          })}
        </nav>
      </div>
    </header>
  );
}
